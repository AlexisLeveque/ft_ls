#include "../libft.h"
#include <string.h>
#include <stdlib.h>

t_dir	*ft_lstnew()
{
	t_dir *new;

	if (!(new = (t_dir*)malloc(sizeof(t_dir))))
		return (NULL);
	new->next = NULL;
	return (new);
}
